from math import sqrt 

#Declaring two variables

num1 = 123.0
num2 = 10.0

#Adding the two variables together

totalSum = num1 + num2
print (totalSum)

#subtracting the two variables together

totalMinus = num1 - num2
print (totalMinus)

#Multiplying the two variables together

totalMult = num1 * num2
print(totalMult)

#Dividing the two variables together

totalDiv = num1 / num2
print(totalDiv)

#Moding the two numbers together

totalMod = num1 % num2
print("When modding the two numbers you get:",totalMod)

#Squareroot of the first number

totalSqrt = sqrt(num1)
print("When squarerooting the first number you get:",totalSqrt)

